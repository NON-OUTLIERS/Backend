const mongoose = require("mongoose");

const Doctors = mongoose.model("doctors", {
  name: {
    type: String,
    required: true,
    trim: true,
  },
  email: {
    type: String,
    required: true,
    trim: true,
  },
  department: {
    type: String,
    required: true,
    trim: true,
  },
});

module.exports = Doctors;
